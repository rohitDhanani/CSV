let addRowToCSV=(dataArr,variables)=>{
    // let TotalTest=dataObj.length;
    let TotalTest=101;

    for(let i=0;i<TotalTest;i++){
        let tempArr=[]
        for(let j=0;j<variables.length;j++){
            if(dataObj.hasOwnProperty(variables[j])){
                tempArr.push(dataObj[variables[j]][i])
            }else{
                tempArr.push('')
            }

        }
        dataArr.push([...tempArr])







    }
    return dataArr.map(row => row.join(',')).join('\n');
}

